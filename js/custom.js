// ==========  START GOOGLE MAP ========== //
function initialize() {
	// modification latitude, longitude //
    var myLatLng = new google.maps.LatLng(44.1318616, 4.069071799999961);

    var mapOptions = {
    	// modification du zoom par défaut //
        zoom: 16,
        center: myLatLng,
        disableDefaultUI: true,
        // je passe scrollwheel à true pour autoriser le zoom sur la carte //
        scrollwheel: true,
        navigationControl: false,
        mapTypeControl: false,
        scaleControl: false,
        // Je passe draggable à true afin de rendre la carte plus interractive //
        draggable: true,
        mapTypeControlOptions: {
            mapTypeIds: [google.maps.MapTypeId.ROADMAP, 'roadatlas']
        }
    };

    var map = new google.maps.Map(document.getElementById('map_canvas'), mapOptions);


    var marker = new google.maps.Marker({
        position: myLatLng,
        map: map,
        icon: 'img/location-icon.png',
        title: '',
    });

}

google.maps.event.addDomListener(window, "load", initialize);
